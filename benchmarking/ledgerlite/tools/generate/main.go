package main

import (
	"bytes"
	"crypto/sha256"
	"encoding/hex"
	"encoding/json"
	"flag"
	"fmt"
	"go/format"
	"os"
)

func main() {
	check := flag.Bool("check", false, "verify generated output without writing")
	flag.Parse()

	spec, err := os.ReadFile("api/openapi.json")
	if err != nil {
		fail(err)
	}
	if !json.Valid(spec) {
		fail(fmt.Errorf("api/openapi.json is not valid JSON"))
	}
	sum := sha256.Sum256(spec)
	generated, err := render(hex.EncodeToString(sum[:]))
	if err != nil {
		fail(err)
	}

	const outputPath = "generated/client.go"
	if *check {
		current, err := os.ReadFile(outputPath)
		if err != nil {
			fail(err)
		}
		if !bytes.Equal(current, generated) {
			fail(fmt.Errorf("%s is stale; run go run ./tools/generate", outputPath))
		}
		return
	}
	if err := os.WriteFile(outputPath, generated, 0o644); err != nil {
		fail(err)
	}
}

func render(specHash string) ([]byte, error) {
	source := fmt.Sprintf(`// Code generated by tools/generate; DO NOT EDIT.
// OpenAPI-SHA256: %s

package generated

type CreatePaymentRequest struct {
	Amount int64 %[2]sjson:"amount"%[2]s
	Currency string %[2]sjson:"currency"%[2]s
}

type Payment struct {
	ID string %[2]sjson:"id"%[2]s
	AccountID string %[2]sjson:"accountId"%[2]s
	Amount int64 %[2]sjson:"amount"%[2]s
	Currency string %[2]sjson:"currency"%[2]s
}

type Error struct {
	Code string %[2]sjson:"code"%[2]s
	Message string %[2]sjson:"message"%[2]s
}
`, specHash, "`")
	return format.Source([]byte(source))
}

func fail(err error) {
	fmt.Fprintln(os.Stderr, err)
	os.Exit(1)
}
